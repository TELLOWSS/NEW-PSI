export const getNativeJobFieldLabel = (jobField: string, policy: ReportLanguagePolicy): string => {
    const source = String(jobField || '').trim();
    const matchedKey = Object.entries(jobKeys).find(([key]) => source.includes(key))?.[1];
    return matchedKey ? nativeJobLabels[policy.code][matchedKey] || policy.genericWork : policy.genericWork;
};

export const getNativeSafetyLevelLabel = (
    safetyLevel: string,
    policy: ReportLanguagePolicy,
): string => {
    if (safetyLevel === '고급') return policy.labels.advanced;
    if (safetyLevel === '중급') return policy.labels.intermediate;
    return policy.labels.beginner;
};

export const hasHangul = (value: string): boolean => /[가-힣]/u.test(String(value || ''));

export const isNativeReportTextClean = (value: string, policy: ReportLanguagePolicy): boolean => {
    const text = String(value || '').trim();
    if (!text) return false;
    if (policy.code !== 'ko' && hasHangul(text)) return false;
    return true;
};
