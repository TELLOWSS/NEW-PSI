const normalized = (value: unknown): string => String(value || '').trim().toLowerCase();

export const resolveReportLanguageCode = (nationality: string, language?: string): ReportLanguageCode => {
    const nation = normalized(nationality);
    const lang = normalized(language);

    if (nation.includes('대한민국') || nation.includes('한국') || nation.includes('korea') || lang.startsWith('ko')) return 'ko';
    if (nation.includes('베트남') || nation.includes('vietnam') || nation.includes('việt') || lang.startsWith('vi')) return 'vi';
    if (nation.includes('중국') || nation.includes('china') || nation.includes('中国') || lang.startsWith('zh') || lang.startsWith('cmn')) return 'zh';
    if (nation.includes('태국') || nation.includes('thailand') || lang.startsWith('th')) return 'th';
    if (nation.includes('미얀마') || nation.includes('myanmar') || nation.includes('burma') || nation.includes('မြန်မာ') || lang.startsWith('my')) return 'my';
    if (nation.includes('우즈벡') || nation.includes('uzbek') || nation.includes('ўзбек') || nation.includes('узбек') || lang.startsWith('uz')) return 'uz';
    if (nation.includes('캄보디아') || nation.includes('cambodia') || nation.includes('កម្ពុជា') || lang.startsWith('km')) return 'km';
    if (nation.includes('인도네시아') || nation.includes('indonesia') || lang.startsWith('id')) return 'id';
    if (nation.includes('말레이시아') || nation.includes('malaysia') || nation.includes('malay') || lang.startsWith('ms')) return 'ms';
    if (nation.includes('몽골') || nation.includes('mongolia') || nation.includes('монгол') || lang.startsWith('mn')) return 'mn';
    if (nation.includes('러시아') || nation.includes('russia') || nation.includes('росси') || nation.includes('русск') || lang.startsWith('ru')) return 'ru';
    if (nation.includes('카자흐') || nation.includes('kazakhstan') || nation.includes('қазақ') || nation.includes('казах') || lang.startsWith('kk')) return 'kk';
    if (nation.includes('네팔') || nation.includes('nepal') || lang.startsWith('ne')) return 'ne';
    return 'en';
};

